# Default separator " " hota hai.

print("Aayush", "Kumar")

# output - Aayush Kumar
# -----------------------------------------------------------------


# change separator :-

# ex1:
print("Aayush", "Kumar", sep="-")

# output - Aayush-Kumar

# ex2:
print(1, 2, 3, 4, 5, sep="->")

# output - 1 -> 2 -> 3 -> 4 -> 5